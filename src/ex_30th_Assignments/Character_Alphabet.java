package ex_30th_Assignments;

public class Character_Alphabet {
    public static void main(String[] args) {
        {
            char c = 'A';
            if((c >= 'a' && c <= 'z')||(c >= 'A' && c <= 'Z'))
                System.out.println("Character is alphabet " +  c);
            else
                System.out.println("Character is not alphabet "+  c);

        }
    }
}


